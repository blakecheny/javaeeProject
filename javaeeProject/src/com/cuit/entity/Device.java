package com.cuit.entity;

public class Device {
	private int uid;
	private int from_lab;
	private String d_type;
	private float price;
	private String brand;
	private String note;
	
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getFrom_lab() {
		return from_lab;
	}
	public void setFrom_lab(int from_lab) {
		this.from_lab = from_lab;
	}
	public String getD_type() {
		return d_type;
	}
	public void setD_type(String d_type) {
		this.d_type = d_type;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float f) {
		this.price = f;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	
	
	
	
}
